    <h1>Arnaud Chapon - 55 ans, bibliothécaire</h1>

    <h2>Parcours politique</h2>
    <p>
        Arnaud Chapon, âgé de 55 ans, est bibliothécaire de profession et s'est impliqué dans la politique locale il y a environ une décennie. Il a notamment participé à des associations citoyennes et à des groupes de réflexion sur l'éducation et la culture, défendant ardemment la préservation du patrimoine culturel et la promotion des bibliothèques publiques. (<a href="https://www.estrepublicain.fr/elections/2024/06/04/europeennes-arnaud-chapon-sur-la-liste-reconquete-de-marion-marechal">L'Est Repub</a>)
    </p>
    <p>
        En 2021, Chapon a rejoint le mouvement Reconquête! d'Éric Zemmour, attiré par ses positions sur l'identité nationale et la souveraineté culturelle. Il a été candidat pour le parti lors des législatives de 2022 dans la 1ère circonscription de la Meuse. Bien qu'il n'ait pas été élu, il a été nommé délégué départemental pour la Meuse. Il a exprimé ses préoccupations concernant l'impact des parcs éoliens sur les paysages locaux et s'est opposé à leur prolifération, estimant qu'ils défigurent le territoire. (<a href="https://www.estrepublicain.fr/elections/2022/05/26/arnaud-chapon-candidat-reconquete-dans-la-1re-circonscription-de-la-meuse">L'Est Repub</a>)
    </p>

