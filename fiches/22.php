    <h1>Olivier Le Coq - 56 ans, chef d’entreprise</h1>

    <h2>Parcours politique</h2>
    <ul>
        <li>
            <strong>Coordinateur Île-de-France :</strong> 
            Olivier Le Coq a été désigné coordinateur pour la région Île-de-France, où il a supervisé les activités et les événements du parti. Par exemple, il a conduit une délégation francilienne de délégués départementaux au Salon de l'Agriculture en 2023 pour accompagner Éric Zemmour.
            <a href="https://fede75.parti-reconquete.fr/article/reconquete-etait-au-salon-de-l-agriculture">(RECONQUÊTE! Paris)</a>
        </li>
        <li>
            <strong>Défense des positions anti-migratoires :</strong> 
            Le Coq est connu pour ses positions fermes contre l'immigration. Il a déclaré que Reconquête! ne cherche pas à répartir les migrants, mais à les faire partir, ce qui reflète l'une des politiques centrales du parti.
            <a href="https://ripostelaique.com/olivier-le-coq-reconquete-ne-veut-pas-repartir-les-migrants-mais-les-faire-partir.html?print=print">(Riposte Laïque)</a>
        </li>
    </ul>

    <h2>Publications</h2>
