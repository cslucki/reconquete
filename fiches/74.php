    <h2>Parcours politique</h2>
    <p>
        Jean-Michel Lamberti, âgé de 60 ans, est un commercial et candidat sur la liste de Reconquête! pour les élections européennes de 2024. Avant de s'engager avec Reconquête!, il a été impliqué dans la politique locale en Corse. En 1989, il figurait sur la liste de Paul Natali pour la mairie de Bastia et, en 2020, sur celle de Jean Zuccarelli. Il est membre du Rassemblement pour la Corse, un mouvement gaulliste fondé en 1985 par Pierre Bartoli.

        Lors des élections législatives de 2022, Lamberti a représenté Reconquête! dans la première circonscription de Haute-Corse. Jean-Michel Lamberti a obtenu 4,79 % des voix, ce score ne lui a pas permis de passer au second tour.

        Il se décrit comme un gaulliste et rejette l'étiquette d'extrême droite souvent attribuée à son parti. Il met l'accent sur la défense des valeurs traditionnelles, la souveraineté nationale, et la lutte contre l'immigration incontrôlée.
    </p>

