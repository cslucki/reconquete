    <h1>Dany Bonnet - 33 ans, chargé de développement</h1>

    <h2>Parcours politique</h2>
    <p>
        Dany Bonnet, 33 ans, est candidat pour le parti Reconquête! aux élections européennes de 2024, représentant la Gironde (33).
        Lors des élections législatives de 2022, Dany Bonnet, candidat pour Reconquête!, a obtenu 3,46 % des voix au premier tour dans la 7ème circonscription de Gironde. Il n'a pas accédé au second tour où Bérangère Couillard (ENS) a été élue avec 52,71 % des voix contre Jean-Renaud Ferran (NUP) (<a href="https://www.interieur.gouv.fr/fr/Elections/Les-resultats/Legislatives/elecresult__legislatives-2022/(path)/legislatives-2022/033/03307.html">Interieur</a>).
    </p>
    <p>
        Bonnet a été invité à plusieurs reprises à des émissions de radio, notamment sur France Bleu Gironde, où il a discuté de l'importance de soutenir les politiques de Reconquête! et d'Éric Zemmour. Son rôle en tant que coordonnateur régional inclut l'organisation de réunions et d'événements pour renforcer la présence du parti dans la région (<a href="https://www.dailymotion.com/video/x878nq1">Dailymotion</a>).
    </p>

