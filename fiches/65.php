    <h1>Nhu-Anh Do - gérante de gîte</h1>

    <h2>Parcours politique</h2>
    <p>
        Nhu-Anh Do, gérante de gîte, est candidate sur la liste de Reconquête! pour les élections européennes de 2024. Elle est déléguée départementale adjointe de l'Yonne pour le parti.
    </p>
    <p>
        Avant de rejoindre Reconquête!, elle a été engagée au sein des Républicains (LR), où elle a servi comme conseillère municipale à Ris-Orangis en Essonne de 2014 à 2020.
    </p>

    <h2>Au niveau personnel</h2>
    <p>
        Originaire du Vietnam, Nhu-Anh Do a effectué la majeure partie de sa carrière dans l'aéronautique, travaillant pour le groupe SAFRAN et comme consultante. Son engagement politique avec Reconquête! est motivé par sa déception vis-à-vis de l'évolution des Républicains et son alignement avec les positions nationalistes et souverainistes prônées par Éric Zemmour (<a href="https://presse-evasion.fr/index.php/component/k2/17-politique/39890-65eme-de-la-liste-de-reconquete-l-icaunaise-nhu-anh-do-veut-une-europe-forte-pour-eviter-le-grand-remplacement">Presse Evasion</a>).
    </p>
    <p>
        Avant de s'engager en politique, elle a eu une carrière notable en tant qu'ingénieur et consultante, puis a travaillé 20 ans dans l'aéronautique. Elle et son mari gèrent désormais un gîte 4 étoiles en Pays d'Othe (<a href="https://www.parenthesechampion.com/">Parenthèse Champion</a>).
    </p>

