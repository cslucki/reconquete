    <h1>David Meyer - 46 ans, avocat</h1>
    <h2>Parcours politique</h2>
    <p>David Meyer, candidat numéro 34 sur la liste de Reconquête! pour les élections européennes de 2024, est avocat de profession basé à Paris. Il a rejoint le parti d'Éric Zemmour en juin 2021.</p>
    <p>Avant cela, il a été candidat pour Reconquête! dans la 9ème circonscription de Paris lors des élections législatives de 2022.</p>
    <h3>Carrière professionnelle</h3>
    <p>David Meyer exerce la profession d'avocat au Barreau de Paris, spécialisé en droit des entreprises et droit de la famille. Son cabinet traite de diverses affaires, notamment le contrôle fiscal, la transmission d'entreprises, et les contrats commerciaux. (<a href="https://www.juritravail.com/avocat/maitre-meyer-david-benjamin">Juritravail</a>).</p>
    <h3>Positionnement</h3>
    <p>Né en Israël, il a grandi dans un milieu religieux avant de s'installer en France à l'âge de 11 ans (<a href="https://legislativesparis2022.blogspot.com/2022/06/rencontre-avec-david-meyer-candidat.html">AFC Legislatives Paris 2017</a>).</p>
    <p>Lors de sa candidature législative, Meyer a exprimé des positions conservatrices, notamment sur la famille et les questions bioéthiques.</p>
