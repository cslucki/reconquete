  <h1>Praince Loubota - Commercial et Candidat pour le Parti Reconquête!</h1>

  <h2>Parcours politique</h2>
  <p>Praince Loubota est un militant actif du parti Reconquête!, fondé par Éric Zemmour. Ce parti est connu pour ses positions nationalistes et identitaires. Loubota a été candidat pour Reconquête! lors des élections législatives de 2022. Il a obtenu 1,79% des voix au premier tour dans la 10e circonscription de Seine-Saint-Denis (<a href="https://www.aulnaylibre.com/2022/05/legislatives-2022-praince-germain-loubota-candidat-pour-eric-zemmour-a-aulnay-sous-bois.html">Aulnaylibre!</a>, <a href="https://www.interieur.gouv.fr/fr/Elections/Les-resultats/Legislatives/elecresult__legislatives-2022/(path)/legislatives-2022/index.html">Interieur Gouv</a>).</p>
  <p>En 2022, lors de ses activités militantes, Loubota a été la cible d'injures racistes de la part de militants d'extrême-gauche, ce qui a fait l'objet d'une altercation notable. Cet incident montre à la fois son engagement politique et les défis qu'il rencontre en raison de ses affiliations (Aulnaycap).</p>

