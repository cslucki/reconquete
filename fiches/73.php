    <h1>Claire-Emmanuelle Gauer - Conseillère régionale de Normandie</h1>

    <h2>Parcours politique</h2>
    <p>
        Claire-Emmanuelle Gauer, âgée de 32 ans, est conseillère régionale de Normandie et professeur de philosophie. Initialement membre du Rassemblement National (RN), elle a quitté ce parti pour rejoindre Reconquête! sous la direction d'Éric Zemmour, aux côtés de Nicolas Bay. Son départ du RN a été motivé par des divergences idéologiques et une déception face à la gestion interne du parti. Elle se positionne comme une patriote de droite et catholique, s'opposant aux dynamiques internes du RN qu'elle juge conflictuelles et contre-productives. Elle plaide pour un soutien envers l’artisanat, l’industrie et l’agriculture, accompagné d’une défense assumée des édifices qui font notre patrimoine.
    </p>

    <h2>Engagements</h2>
    <p>
        En 2021, Claire-Emmanuelle Gauer était diplômée d’un master 2 en philosophie politique de l’université de la Sorbonne. Elle est mère de deux filles et s’est installée dans un village aux alentours de Sées. Actuellement en congé parental, elle enseigne dans un lycée traditionaliste de la cité épiscopale.
    </p>

