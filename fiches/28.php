  <h1>Stéphane Blanchon - 49 ans, conseiller régional d’Auvergne-Rhône-Alpes</h1>

  <h2>Parcours politique</h2>
  <p>Stéphane Blanchon a été élu conseiller régional d’Auvergne-Rhône-Alpes, où il représente les intérêts de ses électeurs en défendant les politiques conservatrices du parti. Il est également candidat pour Reconquête! aux élections européennes de 2024, reflétant son engagement continu dans la politique nationale et européenne.</p>

  <h2>Déclarations controversées</h2>
  <p>Stéphane Blanchon a été au cœur de controverses, notamment lors de son départ du Rassemblement National (RN) pour rejoindre le parti Reconquête! d'Éric Zemmour. Il a critiqué la gestion du RN et affirmé que le parti ne permettait pas la liberté d'expression et l'existence de courants internes.</p>

  <h2>Ses messages sur X</h2>
  <p>Ses tweets abordent principalement les sujets de l'immigration et de la sécurité, adoptant une rhétorique ferme sur l'immigration vue comme un problème sécuritaire ainsi qu'une remise en cause des politiques sanitaires menées pendant la pandémie.</p>

