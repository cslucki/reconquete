    <h1>Laure Pellier - 56 ans, adjointe administrative</h1>

    <h2>Parcours politique</h2>
    <p>
        Laure Pellier, âgée de 56 ans et adjointe administrative, a un parcours politique notable avant de rejoindre Reconquête!.
    </p>
    <p>
        Ancienne secrétaire départementale du Front National (FN) dans la Drôme, elle a adhéré au FN en 2011 et a occupé ce poste de 2015 à 2018. Elle a également été conseillère régionale de 2015 à 2021.
    </p>
    <p>
        Pellier a démissionné de ses fonctions au FN en 2018 pour des raisons personnelles et professionnelles, notamment en raison d'une opportunité de racheter une entreprise hors du département de la Drôme (<a href="https://www.ledauphine.com/politique/2023/12/16/l-ancienne-patronne-du-rn-laure-pellier-rejoint-reconquete">Dauphiné Libéré</a>, <a href="https://france3-regions.francetvinfo.fr/auvergne-rhone-alpes/drome/demission-annoncee-laure-pellier-responsable-du-front-national-drome-1473103.html">France 3 Régions</a>).
    </p>
    <p>
        Depuis sa démission du FN, elle a rejoint Reconquête! où elle continue son engagement politique, soutenant les positions nationalistes et conservatrices du parti fondé par Éric Zemmour. (<a href="https://www.la-croix.com/France/Reconquete-comment-parti-dEric-Zemmour-mise-lagitation-permanente-exister-2023-03-02-1201257401">La Croix</a>)
    </p>

