    <h2>Déclarations controversées</h2>
    <p>Élisabeth Louvel, candidate de Reconquête!, a exprimé des opinions fermes sur la politique migratoire et la sécurité nationale, reprenant souvent les positions d'Éric Zemmour. Elle a été médiatisée pour ses critiques sévères des politiques migratoires actuelles et pour avoir appelé à des mesures plus strictes en matière de sécurité et d'immigration, ce qui a suscité des réactions polarisées <a href="https://www.bretagne5.fr/podcasts/elisabeth-louvel-candidate-aux-europeennes-sur-la-liste-reconquete">Bretagne 5</a>.</p>

    <p>Réfutant l’étiquette d’extrême-droite, elle se préfère de « droite nationale » et envoie des piques à son ancien parti : « Le RN n’a pas forcément abandonné, mais a fait beaucoup de renoncements, a arrondi les angles… »</p>

