    <h1>Jean-Luc Coronel de Boissezon - 56 ans, professeur d’histoire du droit</h1>

    <h2>Parcours politique</h2>
    <p>
        Jean-Luc Coronel de Boissezon, professeur d’histoire du droit, a eu un parcours politique et universitaire marqué par des controverses (<a href="https://fr.wikipedia.org/wiki/Jean-Luc_Coronel_de_Boissezon">Wikipédia</a>).
    </p>
    <p>
        Ancien professeur à l’Université de Montpellier, il est devenu membre du Conseil politique national de Reconquête!.
    </p>

    <h2>Condamnations</h2>
    <p>
        Il a été impliqué dans l'évacuation violente d'un amphithéâtre de la faculté de droit de Montpellier en 2018, où des manifestants occupaient le site pour protester contre la loi ORE.
    </p>
    <p>
        Cette action a conduit à sa condamnation pour violences en réunion, avec une peine de 14 mois de prison dont six fermes, aménagée sous forme de détention à domicile avec bracelet électronique (<a href="https://france3-regions.francetvinfo.fr/occitanie/herault/montpellier/commando-de-la-fac-de-droit-de-montpellier-le-professeur-jean-luc-coronel-condamne-a-un-an-de-prison-avec-sursis-2722850.html">France 3 Régions</a>).
    </p>
    <p>
        Révoqué de l'enseignement supérieur en 2019, sa peine a été réduite à une suspension de quatre ans par le Conseil national de l’enseignement supérieur et de la recherche (CNESER) en 2022. Il a ensuite enseigné à l’ISSEP, école dirigée par Marion Maréchal, avant d'être réintégré à l'Université de Montpellier en 2023, malgré une opposition interne significative (<a href="https://lepoing.net/montpellier-jean-luc-coronel-de-boissezon-peut-reprendre-lenseignement-mais-pas-a-la-fac-de-droit/">Le Poing</a>).
    </p>
    <p>
        Jean-Luc Coronel de Boissezon est également connu pour ses liens avec des groupuscules identitaires comme la Ligue du Midi, ce qui a renforcé la polémique autour de ses activités académiques et politiques  (<a href="https://lepoing.net/montpellier-la-fac-de-droit-opposee-a-la-reintegration-de-coronel-de-boissezon-en-ses-murs/">Le Poing</a>).
    </p>

