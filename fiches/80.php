    <h1>Éric Zemmour - 65 ans, président et fondateur de Reconquête, candidat à l’élection présidentielle de 2022</h1>

    <h2>Parcours politique</h2>
    <p>
        Éric Zemmour, âgé de 65 ans, est le président et fondateur du parti Reconquête! et a été candidat à l'élection présidentielle de 2022. Avant de se lancer en politique, Zemmour était un journaliste et essayiste connu pour ses positions conservatrices et nationalistes.
        Son entrée en politique a été marquée par des discours polarisants sur l'immigration, l'identité nationale et l'islam. Aux élections présidentielles de 2022, Zemmour a obtenu environ 7 % des voix, ce qui, bien que significatif, n'a pas suffi pour accéder au second tour.
    </p>

