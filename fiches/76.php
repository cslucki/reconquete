    <h1>Thierry Perret - 62 ans, retraité</h1>

    <h2>Parcours politique</h2>
    <p>
        Thierry Perret, âgé de 62 ans et retraité, a un parcours politique principalement marqué par son engagement avec le parti Reconquête!.
        Lors des élections législatives de 2022, il s'est présenté dans la première circonscription du Jura après le retrait d'un autre candidat.
        Ancien acheteur dans le secteur du recyclage de métaux industriels, il a mis l'accent sur des thèmes écologiques et souverainistes durant sa campagne (<a href="https://www.leprogres.fr/elections/2022/05/18/legislatives-thierry-perret-mise-sur-l-ecologie-pour-reconquete">Le Progrès</a>) (<a href="https://www.estrepublicain.fr/elections/2022/05/16/franche-comte-decouvrez-les-candidats-dans-votre-circonscription-legislative">L'Est Républicain</a>).
    </p>

