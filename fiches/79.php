    <h1>Ornella Evangelista - 33 ans, responsable événementiel</h1>

    <h2>Parcours politique</h2>
    <p>
        Ornella Evangelista, âgée de 33 ans, est responsable dans le secteur de l'événementiel et candidate pour le parti Reconquête! dirigé par Éric Zemmour. Elle est diplômée de l'université d'Assas en finance et stratégie et a commencé son engagement politique à 20 ans avec les mouvements étudiants de son université.
        Avant de rejoindre Reconquête! en novembre 2021, elle a été adhérente du parti Les Républicains pendant onze ans. Elle a également exercé les fonctions de conseillère municipale d'opposition dans la commune de Sevran, où elle a grandi (<a href="https://legislativesparis2022.blogspot.com/2022/05/rencontre-avec-ornella-evangelista.html">AFC Legislatives Paris 2017</a>).
        Lors des élections législatives de 2022, Ornella Evangelista s'est présentée dans la 13ème circonscription de Paris (15ème Sud). Elle s'est particulièrement investie dans des engagements liés à la famille et à l'éducation, notamment en soutenant l'interdiction de la GPA, la lutte contre l'accès à la pornographie pour les mineurs, et la suppression de l'autorisation préalable pour l'instruction en famille.
        En 2024, Ornella Evangelista est candidate aux élections européennes, représentant le mouvement Reconquête! en Seine-Saint-Denis, figurant en 79ème position sur la liste du parti.
    </p>

