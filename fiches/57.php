  <h1>Leïla Rosenstech - Mère au Foyer et Candidate pour le Parti Reconquête!</h1>

  <h2>Parcours Politique</h2>
  <p>Leïla Rosenstech, 45 ans, est une mère au foyer (8 enfants). Candidate sur la liste de Marion Maréchal aux européennes 2024 - Responsable 7ème circonscription d'Ille-et-Vilaine. Elle a été conseillère municipale à Saint-Coulomb de 2014 à 2020 avant de s'investir pleinement dans la campagne pour les élections législatives de 2022. Elle s'est engagée à lutter contre le déclin économique, industriel et scolaire de la France, et à défendre l'identité nationale face à la pression migratoire.</p>

  <h2>Autres Candidatures</h2>
  <p>Lors des élections législatives de 2022, Leïla Rosenstech, candidate du parti Reconquête! dans la 7ème circonscription d'Ille-et-Vilaine, a obtenu 1,52% des voix (17 votes) au premier tour. Ce résultat ne lui a pas permis d'accéder au second tour.</p>

