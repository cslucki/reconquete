  <h2>Parcours politique</h2>
  <p>Raymond Herbreteau, 74 ans, est le maire des Ventes-de-Bourse dans l'Orne depuis 2008. Il a été élu sous l'étiquette du Front National (FN), devenu Rassemblement National (RN). En plus de son rôle de maire, il s'est présenté aux élections sénatoriales sous la bannière du RN en 2020 et a également été candidat aux élections départementales.</p>

