  <h1>Fanny Judek - Agent Administratif et Candidate pour le Parti Reconquête!</h1>

  <h2>Parcours politique</h2>
  <p>Fanny Judek, âgée de 34 ans, est agent administratif et candidate pour le parti Reconquête! dirigé par Éric Zemmour. Elle a représenté le parti aux élections législatives de 2022 dans la 9e circonscription du Pas-de-Calais, aux côtés d'Axel Cailliaux. Judek s'est engagée dans cette campagne pour promouvoir les idées et les valeurs de Reconquête!, mettant en avant des thèmes tels que la souveraineté nationale, la sécurité et l'identité culturelle (<a href="https://www.lavoixdunord.fr/1186859/article/2022-05-31/legislatives-fanny-judek-et-axel-cailliaux-representeront-reconquete-dans-la-9e">La voix du Nord</a>).</p>

