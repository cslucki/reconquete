    <h1>Karin Hartmann - conseillère municipale de Cagnes-sur-Mer</h1>

    <h2>Parcours politique</h2>
    <p>
        Karin Hartmann est conseillère municipale à Cagnes-sur-Mer et candidate sur la liste de Reconquête! pour les élections européennes de 2024. Son parcours politique est principalement marqué par son engagement au niveau local dans sa commune.
    </p>
    <p>
        Elle a été élue au conseil municipal de Cagnes-sur-Mer, où elle travaille sous la direction du maire Louis Nègre. Son rôle dans le conseil municipal inclut diverses responsabilités en matière de gestion municipale et de développement local (<a href="https://ville.cagnes.fr/les-elus/">Ville de Cagnes-sur-Mer</a>).
    </p>
    <p>
        Son parcours politique inclut sa participation active aux élections départementales de 2021 où elle s'est présentée dans le canton de Cagnes-sur-Mer 2 en binôme avec Jean-Pierre Vincendet sous l'étiquette du Rassemblement National (<a href="https://france3-regions.francetvinfo.fr/provence-alpes-cote-d-azur/alpes-maritimes/departementales-2021-enjeux-et-forces-en-presence-avant-les-elections-dans-les-alpes-maritimes-2119684.html">France 3 Régions</a>).
    </p>

